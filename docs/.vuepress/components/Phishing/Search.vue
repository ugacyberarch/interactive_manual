<script>
import anime from 'animejs'
import Typed from 'typed.js';

export default {

  data() {
    return {
      page: "search",
    }
  },

  mounted() {
    this.start();
  },

  watch: {
    page(newPage) {
      this.$nextTick(this[newPage + "Animation"]);
    },
  },

  methods: {
    start() {
      this.searchAnimation();
    },

    searchAnimation() {
      this.$parent.$emit('change-url', 'https://www.google.com')

      let content = this.$el
        .getBoundingClientRect();
      let field = this.$el
        .querySelector('.typed')
        .getBoundingClientRect();

      var timeline = anime.timeline();
      timeline.add({        // move mouse
        targets: '.mouse',
        elasticity: false,
        easing: 'linear',
        top: field.top - content.top + 10,
        left: field.left - content.left + 200,
      })
      .add({                // type search
        targets: ".input",
        border: '2px solid #ccc',
        complete: () => {
          this.typed = new Typed('.typed', {
            strings: ['netflix^500'],
            typeSpeed: 50,
            showCursor: true,
            onComplete: () => {
              this.page = "results";
            },
          });
        }
      });
    },
    resultsAnimation() {
      let content = this.$el
        .getBoundingClientRect();
      let link = this.$el
        .querySelector('h2')
        .getBoundingClientRect();

      var timeline = anime.timeline();
      timeline.add({
        targets: '.mouse',
        elasticity: false,
        easing: 'easeOutQuint',
        top: link.top - content.top + 10,
        left: link.left - content.left + 100,
        complete: () => {
          let el = this.$el.querySelector('.target');
          el.style.textDecoration = 'underline';
          setTimeout(() => this.page = 'end', 300);
        },
      });
    },
    endAnimation() {
      this.$parent.$emit('change-url', 'https://www.netflix.com')
      setTimeout(() => {
        this.page = 'search';
      } , 1000)
    },
  },
}
